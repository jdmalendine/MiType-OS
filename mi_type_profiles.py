def get_profile(profile_name):
    """
    Retrieves a Mi Type profile by its name.

    Args:
        profile_name (str): The name of the Mi Type profile.

    Returns:
        dict or None: A dictionary containing the profile details if found,
                      otherwise None.
    """
    return MI_TYPE_PROFILES.get(profile_name)

def list_all_profiles():
    """
    Lists the names of all available Mi Type profiles.

    Returns:
        list: A list of strings, where each string is the name of a profile.
    """
    return list(MI_TYPE_PROFILES.keys())

